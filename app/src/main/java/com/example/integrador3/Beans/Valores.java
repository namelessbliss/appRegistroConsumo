package com.example.integrador3.Beans;

public class Valores {
    String Codigo;
    Integer IMC;
    Integer TMB;
    Integer RMB;
    Integer CodUsu;

    public String getCodigo() {
        return Codigo;
    }

    public void setCodigo(String codigo) {
        Codigo = codigo;
    }

    public Integer getIMC() {
        return IMC;
    }

    public void setIMC(Integer IMC) {
        this.IMC = IMC;
    }

    public Integer getTMB() {
        return TMB;
    }

    public void setTMB(Integer TMB) {
        this.TMB = TMB;
    }

    public Integer getRMB() {
        return RMB;
    }

    public void setRMB(Integer RMB) {
        this.RMB = RMB;
    }

    public Integer getCodUsu() {
        return CodUsu;
    }

    public void setCodUsu(Integer codUsu) {
        CodUsu = codUsu;
    }
}
